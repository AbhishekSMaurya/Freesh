// MongoDB Connection
const connectDB = async () => {
  try {
    await mongoose.connect(process.env.MONGODB_URI);
    console.log('âœ… MongoDB Connected Successfully');
  } catch (error) {
    console.error('âŒ MongoDB Connection Error:', error);
    process.exit(1);
  }
};

// Start Server
const PORT = process.env.PORT || 5000;

